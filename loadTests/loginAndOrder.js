        'access-control-request-method': 'GET',
        'cache-control': 'no-cache',
        origin: 'https://pizza.climb-5.com',
        priority: 'u=1, i',
        'sec-fetch-dest': 'empty',
        'sec-fetch-mode': 'cors',
        'sec-fetch-site': 'same-site',
